package uni1a;

public class Temporada {
    private int num_Tepo;
    private int num_episodios;

    public Temporada(int num_Tepo, int num_episodios) {
        this.num_Tepo = num_Tepo;
        this.num_episodios = num_episodios;
    }

    public int getNum_Tepo() {
        return num_Tepo;
    }

    public int getNum_episodios() {
        return num_episodios;
    }
}
